package fundamentos;

public class AbreviandoOperadoresAritmetico {
    public static void main(String[] args) {
        int quantidadeProduto = 10;

        quantidadeProduto +=  3;

        quantidadeProduto -= 2;

        quantidadeProduto *= 4;

        quantidadeProduto /= 2;

        quantidadeProduto %= 5;

        System.out.println(quantidadeProduto);
    }
}
